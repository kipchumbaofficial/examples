#include <stdio.h>
/**
 * main - Prints size of for basic data types in C
 *
 * Return: 0
 */
int main(void)
{
	printf("Size of 'char' in my computer is: %lu\n", sizeof(char));
	printf("Size of 'int' in my computer is: %lu\n", sizeof(int));
	printf("Size of 'float' in my computer is: %lu\n", sizeof(float));
	printf("Size of 'double' in my computer is: %lu\n", sizeof(double));
	return (0);
}
